                                            firmware are up-to-date, applying updates to improve functionality and
                                            security. Maintenance includes applying updates and verifying stability.
                                        </li>
                                        <li><strong>Battery Backup Systems:</strong> Testing battery backup systems for
                                            operational readiness during power outages. Maintenance involves replacing
                                            old batteries and testing efficiency.</li>
                                        <li><strong>IoT Integration:</strong> Assessing IoT device integration with the
                                            fire alarm system, ensuring accurate communication and data utilization.
                                            Maintenance involves calibration and integration checks.</li>
                                        <li><strong>Sprinkler System Monitoring:</strong> Testing monitoring systems for
                                            sprinklers to ensure they provide early warnings in case of malfunctions.
                                            Maintenance includes checking flow sensors and alarms.</li>
                                        <li><strong>Elevator Recall:</strong> Verifying elevator recall systems to
                                            ensure elevators return to a designated floor and remain inoperative during
                                            a fire. Maintenance includes testing functionality and compliance.</li>
                                        <li><strong>HVAC Shunts:</strong> Testing HVAC shunt systems to ensure proper
                                            operation during a fire alarm. This includes verifying that HVAC systems are
                                            correctly shut down or adjusted to prevent smoke spread and support fire
                                            containment. Maintenance involves checking shunt functionality and ensuring
                                            compliance with fire safety standards.</li>
                                        <li><strong>Pull Stations:</strong> Inspecting and testing manual pull stations
                                            for accessibility and functionality. Maintenance includes checking the
                                            physical condition and connection.</li>
                                        <li><strong>Visual Annunciators:</strong> Testing visual annunciators like
                                            strobes to ensure they provide clear visual alerts during emergencies.
                                            Maintenance includes checking functionality and replacing faulty units.</li>
                                        <li><strong>Audible Annunciators:</strong> Testing audible annunciators like
                                            bells to ensure they provide clear and effective sound alerts. Maintenance
                                            includes verifying sound levels and replacing malfunctioning units.</li>
                                        <li><strong>Device Monitoring:</strong> Ensuring all fire alarm system devices
                                            are monitored for performance and connectivity. Maintenance involves
                                            addressing any issues detected.</li>
